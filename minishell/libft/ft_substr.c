#include "libft.h"

char	*ft_substr(char const *s, unsigned int start, size_t len)
{
	size_t	i;
	char	*sub;

	if (!s)
		return (0);
	sub = (char *)malloc(len + 1);
	if (!sub)
		return (0);
	if (ft_strlen(s) < start)
	{
		*sub = 0;
		return (sub);
	}
	i = 0;
	while (i < len)
	{
		sub[i] = s[start + i];
		i++;
	}
	sub[i] = 0;
	return (sub);
}
